# Meus primeiros códigos Python

### Textos

print("Olá, mundo!")

### Contas matemáticas

print(5 + 3)
print(8 * 2)
print(5**3) # 5 elevado a 3
print(8 / 2)

### Criação de variáveis

a = 4
b = 6

soma = a + b
print(soma)

quadrado = soma**2
print(quadrado)

print("A soma é:", soma, "e o quadrado da soma é:", quadrado)

### False e True

print(soma==8)

### If e else

if soma == 8:
    print("A soma é 8!")
else:
    print("A soma não é 8!")

